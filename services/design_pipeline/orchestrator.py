"""
services/design_pipeline/orchestrator.py — AI Design-to-Code Orchestrator
IL-D2C-01 | BANXE EMI AI Bank

Bridges Penpot MCP context with AI code generation.

Pipeline:
  1. Fetch design context from Penpot (component + tokens)
  2. Build structured LLM prompt
  3. LLM generates Mitosis JSX
  4. Compile via Mitosis to target framework
  5. Visual QA screenshot comparison

LLM: Ollama via LangChain (local, qwen2.5-coder:7b by default).
No float for financial values that might appear in UI forms.

FCA/EU AI Act Art.52: AI-generated UI code must be transparently labelled.
"""

